from Or import or_gate

def or16(input1_bits, input2_bits):
    """
    Returns a 16-bit value where each bit is True (1) if either of the corresponding bits in the two inputs are True (1).
    """
    return [int(or_gate(input1_bits[bit], input2_bits[bit])) for bit in range(len(input2_bits))]

